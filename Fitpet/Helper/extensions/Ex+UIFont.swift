import UIKit

extension UIFont {
  static func appleSDGothicBold(ofSize size: CGFloat) -> UIFont {
    return UIFont(name: "AppleSDGothicNeo-Bold", size: size)!
  }
  static func appleSDGothicRegular(ofSize size: CGFloat) -> UIFont {
    return UIFont(name: "AppleSDGothicNeo-Regular", size: size)!
  }
}
